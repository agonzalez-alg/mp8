document.getElementById("sol").addEventListener('click', sol);
document.getElementById("luna").addEventListener('click', luna);

function sol() {
    document.body.style.background="orange";
    document.getElementById("luna").style.background="orange";
}

function luna() {
    document.body.style.background="midnightblue";
    document.getElementById("luna").style.background="midnightblue";
}
